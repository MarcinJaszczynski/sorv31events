    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $validated = $request->validate(
            [
                'event_id' => 'required',
                'contractor_first_name' => 'required|max:80',
                'contractor_last_name' => 'required|max:80',
                'contractor_email' => 'required|max:120',
                'contractor_contact_phone' => 'required|max:80',
                'tour_participant_first_name' => 'required|max:80',
                'tour_participant_last_name' => 'required|max:80',
            ]
        );
        $validated['event_id'] = settype($validated['event_id'], "integer");
        $contract = Contract::create($validated);
        $contract->save();
        return redirect('/contracts');
    }

